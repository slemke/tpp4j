package de.fhkoeln.datatypes;

/**
 * Container class that provides Strings for the support mediatypes used within the pipeline.
 * @author Sascha Lemke
 */
public class MediaType {

    /**
     * String that describes the image/gif mediatyp.
     */
    public final static String IMAGE_GIF = "image/gif";
    /**
     * String that describes the image/jpeg mediatyp.
     */
    public final static String IMAGE_JPEG = "image/jpeg";
    /**
     * String that describes the image/png mediatyp.
     */
    public final static String IMAGE_PNG = "image/png";
    /**
     * String that describes the image/tiff mediatyp.
     */
    public final static String IMAGE_TIFF = "image/tiff";
    /**
     * String that describes the text/plain mediatyp.
     */
    public final static String TEXT_PLAIN = "text/plain";
}
